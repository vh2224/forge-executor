import { describe, test } from "node:test";
import assert from "node:assert/strict";
import { mkdtempSync, mkdirSync, writeFileSync, readFileSync, rmSync, existsSync } from "node:fs";
import { tmpdir, homedir } from "node:os";
import { join } from "node:path";
import { classifyStateLayout, classifyMilestoneStateLayouts } from "../migrate/state-layout.ts";

function withSandbox<T>(fn: (dir: string) => T): T {
  const dir = mkdtempSync(join(tmpdir(), "forge-migrate-state-layout-test-"));
  try {
    return fn(dir);
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

function writeGsdState(cwd: string, content: string): void {
  const gsdDir = join(cwd, ".gsd");
  mkdirSync(gsdDir, { recursive: true });
  writeFileSync(join(gsdDir, "STATE.md"), content);
}

function writeMilestoneState(cwd: string, milestoneId: string, fileName: string, content: string): void {
  const dir = join(cwd, ".gsd", "milestones", milestoneId);
  mkdirSync(dir, { recursive: true });
  writeFileSync(join(dir, fileName), content);
}

// в”Ђв”Ђ classifyStateLayout вЂ” synthetic в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

describe("classifyStateLayout вЂ” synthetic", () => {
  test("absent вЂ” no .gsd/STATE.md at all", () => {
    withSandbox((dir) => {
      const finding = classifyStateLayout(dir);
      assert.equal(finding.kind, "absent");
      assert.ok(finding.detail.length > 0);
    });
  });

  test("twoPointZero вЂ” fenced ```yaml block with non-empty milestone", () => {
    withSandbox((dir) => {
      writeGsdState(
        dir,
        ["# STATE", "", "```yaml", "milestone: teste", "phase: executing", "```", ""].join("\n"),
      );
      const finding = classifyStateLayout(dir);
      assert.equal(finding.kind, "twoPointZero");
    });
  });

  test("dashboard1x вЂ” synthetic AUTO-GENERATED marker, no fenced yaml", () => {
    withSandbox((dir) => {
      writeGsdState(
        dir,
        [
          "<!-- AUTO-GENERATED by scripts/forge-dashboard.js вЂ” do not edit by hand -->",
          "<!-- Last regen: 2026-07-08T17:51:27.076Z -->",
          "",
          "# GSD Dashboard",
          "",
          "No active runs.",
          "Last completed: M001 вЂ” algo qualquer.",
          "",
        ].join("\n"),
      );
      const finding = classifyStateLayout(dir);
      assert.equal(finding.kind, "dashboard1x");
    });
  });

  test("unknown вЂ” content matches none of the known shapes, never throws", () => {
    withSandbox((dir) => {
      writeGsdState(dir, "algo completamente aleatГіrio sem forma reconhecГ­vel\n");
      const finding = classifyStateLayout(dir);
      assert.equal(finding.kind, "unknown");
    });
  });
});

// в”Ђв”Ђ classifyMilestoneStateLayouts вЂ” synthetic в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

describe("classifyMilestoneStateLayouts вЂ” synthetic", () => {
  test("missing .gsd/milestones/ в†’ [] (never throws)", () => {
    withSandbox((dir) => {
      const findings = classifyMilestoneStateLayouts(dir);
      assert.deepEqual(findings, []);
    });
  });

  test("frontmatter1x вЂ” long-form `<mid>-STATE.md` name", () => {
    withSandbox((dir) => {
      writeMilestoneState(
        dir,
        "M999",
        "M999-STATE.md",
        [
          "---",
          "milestone: M999",
          "kind: milestone",
          "status: complete",
          "---",
          "",
          "# M999 State",
          "",
          "**Active Slice:** вЂ”",
          "**Active Task:** вЂ”",
          "",
        ].join("\n"),
      );
      const findings = classifyMilestoneStateLayouts(dir);
      assert.equal(findings.length, 1);
      assert.equal(findings[0].kind, "frontmatter1x");
      assert.ok(findings[0].path.endsWith(join("M999", "M999-STATE.md")));
    });
  });

  test("frontmatter1x вЂ” short-form `STATE.md` name", () => {
    withSandbox((dir) => {
      writeMilestoneState(
        dir,
        "M998",
        "STATE.md",
        ["---", "milestone: M998", "---", "", "**Active Slice:** S01", ""].join("\n"),
      );
      const findings = classifyMilestoneStateLayouts(dir);
      assert.equal(findings.length, 1);
      assert.equal(findings[0].kind, "frontmatter1x");
      assert.ok(findings[0].path.endsWith(join("M998", "STATE.md")));
    });
  });

  test("unknown вЂ” milestone dir has a STATE file that matches neither shape", () => {
    withSandbox((dir) => {
      writeMilestoneState(dir, "M997", "M997-STATE.md", "conteГєdo sem frontmatter nem Active Slice\n");
      const findings = classifyMilestoneStateLayouts(dir);
      assert.equal(findings.length, 1);
      assert.equal(findings[0].kind, "unknown");
    });
  });

  test("milestone dir with no STATE file at all is skipped, not reported", () => {
    withSandbox((dir) => {
      const dir996 = join(dir, ".gsd", "milestones", "M996");
      mkdirSync(dir996, { recursive: true });
      writeFileSync(join(dir996, "M996-ROADMAP.md"), "# roadmap\n");
      const findings = classifyMilestoneStateLayouts(dir);
      assert.equal(findings.length, 0);
    });
  });
});

