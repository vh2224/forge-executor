/**
 * Forge migrate — coverage for `apply.ts` (S02/T06): the `--apply`
 * orchestrator. Proves the HARD RULE (ROADMAP, incident 2026-06-10 of the
 * real 1.0) end-to-end: `backupGsdTree` (T01) runs exactly once, always
 * BEFORE any of the four `applyX` converters (T02-T05), and only when
 * `buildMigrateReport` (S01) shows something genuinely needs converting.
 *
 * Same isolated-home discipline as `migrate-prefs-convert.test.ts` — `HOME`/
 * `FORGE_HOME` are sandboxed so a real machine's `~/.claude/forge-agent-
 * prefs.md`/`gsdHome()/prefs.md` never contaminates these cases (prefs is one
 * of the four dimensions `applyMigration` decides on).
 */

import { describe, test } from "node:test";
import assert from "node:assert/strict";
import { mkdtempSync, mkdirSync, writeFileSync, readFileSync, readdirSync, rmSync, statSync, existsSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { applyMigration, formatApplyReport, type ApplyReport } from "../migrate/apply.ts";

function withIsolatedHome<T>(fn: (fakeHome: string) => T): T {
  const fakeHome = mkdtempSync(join(tmpdir(), "forge-migrate-apply-home-"));
  const prevHome = process.env.HOME;
  const prevForgeHome = process.env.FORGE_HOME;
  process.env.HOME = fakeHome;
  process.env.FORGE_HOME = join(fakeHome, ".forge");
  try {
    return fn(fakeHome);
  } finally {
    if (prevHome === undefined) delete process.env.HOME;
    else process.env.HOME = prevHome;
    if (prevForgeHome === undefined) delete process.env.FORGE_HOME;
    else process.env.FORGE_HOME = prevForgeHome;
    rmSync(fakeHome, { recursive: true, force: true });
  }
}

function withSandbox<T>(fn: (dir: string) => T): T {
  const dir = mkdtempSync(join(tmpdir(), "forge-migrate-apply-test-"));
  try {
    return fn(dir);
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

function withIsolatedHomeAndSandbox<T>(fn: (dir: string) => T): T {
  return withIsolatedHome(() => withSandbox(fn));
}

function backupDirsIn(dir: string): string[] {
  return readdirSync(dir).filter((name) => name.startsWith(".gsd-backup-"));
}

function snapshotTree(root: string): string[] {
  const out: string[] = [];
  function walk(dir: string): void {
    for (const name of readdirSync(dir).sort()) {
      const full = join(dir, name);
      const st = statSync(full);
      if (st.isDirectory()) {
        walk(full);
      } else {
        out.push(`${full.slice(root.length)}:${st.size}:${readFileSync(full, "utf-8")}`);
      }
    }
  }
  walk(root);
  return out;
}

function writeNativeGsdFixture(cwd: string): void {
  const gsdDir = join(cwd, ".gsd");
  mkdirSync(gsdDir, { recursive: true });
  writeFileSync(join(gsdDir, "STATE.md"), "# STATE\n\n```yaml\nmilestone: M900\n```\n");
}

function writeMixedGsdFixture(cwd: string): void {
  const gsdDir = join(cwd, ".gsd");
  mkdirSync(gsdDir, { recursive: true });
  writeFileSync(
    join(gsdDir, "STATE.md"),
    [
      "<!-- AUTO-GENERATED by scripts/forge-dashboard.js — do not edit by hand -->",
      "",
      "# GSD Dashboard",
      "",
      "No active runs.",
      "",
    ].join("\n"),
  );

  const decisionsDir = join(gsdDir, "decisions");
  mkdirSync(decisionsDir, { recursive: true });
  writeFileSync(
    join(decisionsDir, "M001.md"),
    [
      "---",
      "unit_id: M001",
      "decisions:",
      "  - when: 2026-01-01",
      "    scope: teste",
      "    decision: algo",
      "    choice: A",
      "    rationale: porque sim",
      "    revisable: true",
      "---",
      "",
    ].join("\n"),
  );
}

