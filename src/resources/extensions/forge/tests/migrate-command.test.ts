describe("zero-write proof — buildMigrateReport + formatMigrateReport over a mixed forge 1.0 fixture", () => {
  test("nenhum arquivo sob .gsd/ é criado, apagado ou modificado durante a chamada", () => {
    withSandbox((dir) => {
      writeMixedGsdFixture(dir);
      const before = snapshotTree(join(dir, ".gsd"));

      const report = buildMigrateReport(dir);
      const text = formatMigrateReport(report);

      const after = snapshotTree(join(dir, ".gsd"));
      assert.deepEqual(after, before, ".gsd/ tree must be byte-identical before and after the dry-run call");

      // Sanity: the fixture was actually classified as forge 1.0 shapes (not
      // silently skipped), so the zero-write assertion above is meaningful.
      assert.equal(report.stateTop.kind, "dashboard1x");
      assert.match(text, /dashboard forge 1\.0/);
      assert.equal(report.roadmap.length, 1);
      assert.equal(report.roadmap[0].kind, "prose1x");
      assert.match(text, /M100-ROADMAP\.md/, "roadmap (6th dimension) reported in the dry-run text");
    });
  });
});

// ── Subcommand routing — structural assertion over forge-command.ts source ──
//
// Per T04-PLAN Step 6: building a full ExtensionCommandContext mock just to
// cover one trivial routing line is not worth the fragility. Instead, assert
// structurally that `case "migrate":` exists, is wired to
// buildMigrateReport/formatMigrateReport via ctx.ui.notify, and never touches
// newSession/updateState/writeFileSync.

describe("/forge migrate — subcommand routing (structural)", () => {
  const source = readFileSync(
    new URL("../commands/forge-command.ts", import.meta.url),
    "utf-8",
  );

  test("'migrate' is registered in SUBCOMMANDS", () => {
    assert.match(source, /SUBCOMMANDS\s*=\s*\[[^\]]*"migrate"[^\]]*\]/);
  });

  test("case \"migrate\" renders the report to stdout (print/headless) or ctx.ui.notify (TUI), zero writes", () => {
    // External-review fix 5b506474: notify is a TUI surface, MUTE in --print —
    // the PRIMARY consumer of a dry-run. The case now branches on
    // isPrintHeadlessContext: stdout for headless, notify for the TUI.
    //
    // S02/T06: the case now ALSO contains an `if (rest[0] === "--apply")`
    // branch that legitimately mentions applyMigration/updateState-adjacent
    // strings — so the zero-write proof below is scoped to ONLY the `else`
    // sub-block (the dry-run path), not the whole case body.
    const caseMatch = source.match(/case "migrate": \{\s*\n([\s\S]*?)\n\s*\}\s*\n\s*case "(?:review|accounts|models|unblock)"/);
    assert.ok(caseMatch, "expected a case \"migrate\": { … } block before case \"unblock\"");
    const body = caseMatch![1];
    assert.doesNotMatch(body, /newSession/, "migrate must never reach ctx.newSession — dry-run or --apply, zero-write guarantee");

    const elseMatch = body.match(/\}\s*else\s*\{\s*\n([\s\S]*?)\n\s*\}\s*\n\s*return;/);
    assert.ok(elseMatch, "expected an `} else { … }` dry-run sub-block before the case's final return");
    const dryRunBody = elseMatch![1];

    assert.match(dryRunBody, /isPrintHeadlessContext\(ctx\)/);
    assert.match(dryRunBody, /process\.stdout\.write\(/);
    assert.match(dryRunBody, /ctx\.ui\.notify\(/);
    assert.match(dryRunBody, /formatMigrateReport\(/);
    assert.match(dryRunBody, /buildMigrateReport\(ctx\.cwd\)/);
    assert.doesNotMatch(dryRunBody, /newSession/, "migrate dry-run must never reach ctx.newSession — zero-write guarantee");
    assert.doesNotMatch(dryRunBody, /updateState/, "migrate dry-run must never call state/store.ts:updateState");
    assert.doesNotMatch(dryRunBody, /writeFileSync/, "migrate dry-run must never write to disk directly");
    assert.doesNotMatch(dryRunBody, /applyMigration/, "migrate dry-run must never call applyMigration");
    assert.doesNotMatch(dryRunBody, /backupGsdTree/, "migrate dry-run must never call backupGsdTree");
  });

  test("case \"migrate\" --apply sub-block calls applyMigration/formatApplyReport", () => {
    const caseMatch = source.match(/case "migrate": \{\s*\n([\s\S]*?)\n\s*\}\s*\n\s*case "(?:review|accounts|models|unblock)"/);
    assert.ok(caseMatch, "expected a case \"migrate\": { … } block before case \"unblock\"");
    const body = caseMatch![1];

    const applyMatch = body.match(/if \(rest\[0\] === "--apply"\) \{\s*\n([\s\S]*?)\n\s*\}\s*else\s*\{/);
    assert.ok(applyMatch, "expected an `if (rest[0] === \"--apply\") { … } else { … }` sub-block");
    const applyBody = applyMatch![1];

    assert.match(applyBody, /applyMigration\(/);
    assert.match(applyBody, /formatApplyReport\(/);
    assert.match(applyBody, /isPrintHeadlessContext\(ctx\)/);
  });

  test("formatHelp() lists 'migrate' with a descriptive line", () => {
    assert.match(source, /"\s*migrate\s*—[^"]*dry-run[^"]*"/);
  });
});
