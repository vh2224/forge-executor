/**
 * Forge migrate — coverage for `buildMigrateReport`/`formatMigrateReport`
 * (T04, `migrate/report.ts`) and the `/forge migrate` subcommand's zero-write
 * guarantee.
 *
 * Same one-shot-copy / mkdtemp-sandbox discipline as
 * `migrate-state-layout.test.ts` (T01) — nothing here mutates a real
 * project's `.gsd/` tree.
 *
 * The full `case "migrate":` handler in `commands/forge-command.ts` needs a
 * real `ExtensionCommandContext`, which this slice's tests avoid building
 * from scratch (see `forge-command.test.ts`'s own `fakeCtx` for the closest
 * precedent). Per T04-PLAN Step 6, the zero-write proof below exercises the
 * REAL I/O path directly — `buildMigrateReport` + `formatMigrateReport` — and
 * the subcommand routing is instead asserted structurally against the
 * source text of `forge-command.ts`.
 */

import { describe, test } from "node:test";
import assert from "node:assert/strict";
import { mkdtempSync, mkdirSync, writeFileSync, readFileSync, readdirSync, rmSync, statSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { buildMigrateReport, formatMigrateReport, type MigrateReport } from "../migrate/report.ts";

function withSandbox<T>(fn: (dir: string) => T): T {
  const dir = mkdtempSync(join(tmpdir(), "forge-migrate-command-test-"));
  try {
    return fn(dir);
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

// ── buildMigrateReport — empty sandbox ───────────────────────────────────────

describe("buildMigrateReport — empty sandbox (no .gsd/ at all)", () => {
  test("never throws and every cwd-scoped field degrades to empty/absent", () => {
    withSandbox((dir) => {
      let report: MigrateReport | undefined;
      assert.doesNotThrow(() => {
        report = buildMigrateReport(dir);
      });

      assert.equal(report!.stateTop.kind, "absent");
      assert.deepEqual(report!.milestoneStates, []);
      // `prefs` reuses `prefsSources(cwd)` (T02), which includes two layers
      // anchored to the REAL home directory (`~/.claude/forge-agent-prefs.md`,
      // `gsdHome()/prefs.md`) that are not sandboxable by passing a fake cwd —
      // see the same caveat documented in migrate-prefs-layout.test.ts (T01/T02).
      // Only the cwd-anchored "repo"/"local" layers are guaranteed absent here.
      const cwdScopedPrefs = report!.prefs.filter((p) => p.source.startsWith(dir));
      assert.deepEqual(cwdScopedPrefs, []);
      assert.equal(report!.fragmentStores.length, 3);
      for (const store of report!.fragmentStores) {
        assert.deepEqual(store.files, []);
      }
      assert.deepEqual(report!.orphans, []);
      assert.deepEqual(report!.roadmap, []);
    });
  });

  test("formatMigrateReport on the degraded empty report still produces the dry-run line, never throws", () => {
    withSandbox((dir) => {
      const report = buildMigrateReport(dir);
      let text = "";
      assert.doesNotThrow(() => {
        text = formatMigrateReport(report);
      });
      assert.match(text, /Nenhuma escrita foi realizada \(dry-run\)/);
    });
  });
});

